console.log('abs: ', Math.abs(-12)) // 12, abs → absolute (절대값) 

console.log('min: ', Math.min(2, 8)) // 2

console.log('max: ', Math.max(2, 8)) // 8

console.log('ceil: ', Math.ceil(3.14)) // 4 올림

console.log('floor: ', Math.floor(3.14)) // 3 내림

console.log('round: ', Math.round(3.5)) // 4 반올림

console.log('random ', Math.random()) // 0.92045647879121, 랜덤으로 출력